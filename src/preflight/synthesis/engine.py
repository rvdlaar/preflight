def format_persona_findings(persona_findings: list[dict]) -> str:
    sections = []
    overview_rows = []

    for i, pf in enumerate(persona_findings):
        name = pf.get("name", "?")
        role = pf.get("role", "?")
        rating = pf.get("rating", "?")
        authority = pf.get("authority")
        rating_nl = RATING_NL.get(rating, rating)

        sections.append(f"### 6.{i + 1} {name} — {role}")
        sections.append("")
        sections.append(f"**Beoordeling / Rating:** {rating_nl}")
        sections.append("")

        findings = pf.get("findings", [])
        if findings:
            sections.append("**Bevindingen / Findings:**")
            for f in findings:
                sections.append(f"- {f}")
            sections.append("")

        conditions = pf.get("conditions", [])
        if conditions:
            sections.append("**Voorwaarden / Conditions:**")
            for c in conditions:
                sections.append(f"- {c}")
            sections.append("")

        sections.append("---")
        sections.append("")

        authority_nl = AUTHORITY_NL.get(authority, "-") if authority else "-"
        overview_rows.append(f"| {role} | {name} | {rating_nl} | {authority_nl} |")

    overview = [
        "### Beoordelingsoverzicht / Assessment Overview",
        "",
        "| Persona | Naam | Rating | Bijzondere bevoegdheid |",
        "|---------|------|--------|------------------------|",
        *overview_rows,
        "",
    ]

    return "\n".join([*overview, *sections])


def generate_risk_register(persona_findings: list[dict]) -> str:
    rows = []
    n = 1
    for pf in persona_findings:
        if pf.get("rating") in ("approve", "na"):
            continue
        for finding in pf.get("findings", []):
            risk = "H" if pf["rating"] == "block" else "M"
            rows.append(
                f"| R{n} | {finding} | {risk} | {risk} | {risk} | {pf['name']} | [mitigerende maatregel] |"
            )
            n += 1

    if not rows:
        return "| # | Risico | Kans | Impact | Score | Bron (persona) | Mitigatie |\n|---|--------|------|--------|-------|----------------|-----------|\n| - | Geen risico's geïdentificeerd | - | - | - | - | - |"

    header = "| # | Risico | Kans | Impact | Score | Bron (persona) | Mitigatie |\n|---|--------|------|--------|-------|----------------|-----------|"
    return f"{header}\n" + "\n".join(rows)


def generate_conditions_table(persona_findings: list[dict]) -> str:
    rows = []
    n = 1
    for pf in persona_findings:
        for cond in pf.get("conditions", []):
            rows.append(
                f"| C{n} | {cond} | {pf['name']} | [eigenaar] | [datum] | Open |"
            )
            n += 1

    if not rows:
        return "| # | Voorwaarde | Bron (persona) | Eigenaar | Deadline | Status |\n|---|-----------|----------------|----------|----------|--------|\n| - | Geen voorwaarden | - | - | - | - |"

    header = "| # | Voorwaarde | Bron (persona) | Eigenaar | Deadline | Status |\n|---|-----------|----------------|----------|----------|--------|"
    return f"{header}\n" + "\n".join(rows)


def append_disclaimer(text: str) -> str:
    return f"{text}\n\n---\n\n{ACCOUNTABILITY_DISCLAIMER}"
