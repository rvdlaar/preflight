"""
Preflight synthesis engine — templates, citations, documents.

Port of synthesis/engine.mjs to idiomatic Python.
Templates are Markdown files with Jinja2-style {{ placeholder }} syntax.
Citations use [§P:name] and [§K:id] format.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from datetime import date
from pathlib import Path
from typing import Any

TEMPLATES_DIR = Path(__file__).parent.parent.parent.parent / "templates"

RATING_NL = {
    "approve": "Akkoord",
    "conditional": "Voorwaardelijk",
    "concern": "Bezorgd",
    "block": "Blokkade",
    "na": "N.v.t.",
}

AUTHORITY_NL = {
    "VETO": "Vetorecht",
    "ESCALATION": "Escalatie",
    "INDEPENDENT": "Onafhankelijk",
    "CHALLENGE": "Aanvechtging",
    "PATIENT_SAFETY": "Patiëntveiligheid",
}

ACCOUNTABILITY_DISCLAIMER = (
    "> **Disclaimer:** This is a draft assessment generated by Preflight using AI personas.\n"
    "> The architect owns the final assessment. The board owns the decision.\n"
    "> Preflight provides analysis, not judgment. All findings require human verification.\n"
    "> Citations marked [§P:name] come from persona assessments. "
    "Citations marked [§K:id] come from the knowledge base.\n"
    "> Uncited claims are flagged with [VERIFY] and must be confirmed by the architect."
)


# ---------------------------------------------------------------------------
# Template loading & resolution
# ---------------------------------------------------------------------------


def load_template(name: str) -> str:
    path = TEMPLATES_DIR / f"{name}.md"
    return path.read_text(encoding="utf-8")


def resolve_placeholders(template: str, context: dict[str, Any]) -> str:
    def _resolve(obj: Any, path: str) -> Any:
        for key in path.split("."):
            if isinstance(obj, dict):
                obj = obj.get(key)
            else:
                return None
        return obj

    def _replace(match: re.Match) -> str:
        path = match.group(1)
        value = _resolve(context, path)
        return str(value) if value is not None else match.group(0)

    return re.sub(r"\{\{(\w[\w.]*)\}\}", _replace, template)


