def cite_persona(name: str, field: str | None = None) -> str:
    if field:
        return f"[§P:{name}.{field}]"
    return f"[§P:{name}]"


def cite_knowledge(source_id: str, excerpt: str | None = None) -> str:
    if excerpt:
        return f'[§K:{source_id}: "{excerpt}"]'
    return f"[§K:{source_id}]"


@dataclass
class CitationResult:
    persona: list[dict] = field(default_factory=list)
    knowledge: list[dict] = field(default_factory=list)


def parse_citations(text: str) -> CitationResult:
    persona = [
        {"name": m.group(1), "field": m.group(2), "index": m.start()}
        for m in re.finditer(r"\[§P:([A-Za-z-]+)(?:\.([a-z]+))?\]", text)
    ]
    knowledge = [
        {"id": m.group(1), "excerpt": m.group(2), "index": m.start()}
        for m in re.finditer(r"\[§K:([a-z0-9-]+)(?:: \"([^\"]+)\")?\]", text)
    ]
    return CitationResult(persona=persona, knowledge=knowledge)


def strip_citations(text: str) -> str:
    text = re.sub(r"\[§P:[A-Za-z-]+(?:\.[a-z]+)?\]", "", text)
    text = re.sub(r"\[§K:[a-z0-9-]+(?:: \"[^\"]+\")?\]", "", text)
    return re.sub(r"  +", " ", text).strip()


def verify_citations(text: str, context: dict) -> dict:
    citations = parse_citations(text)
    participating = {pf["name"] for pf in context.get("persona_findings", [])}
    available_sources = {s["id"] for s in context.get("sources", [])}
    cited_personas = {c["name"] for c in citations.persona}
    cited_sources = {c["id"] for c in citations.knowledge}

    unreferenced = [
        f'Persona "{c["name"]}" cited but did not participate in assessment'
        for c in citations.persona
        if c["name"] not in participating
    ] + [
        f'Source "{c["id"]}" cited but not in retrieved knowledge'
        for c in citations.knowledge
        if c["id"] not in available_sources
    ]

    uncited = [p for p in participating if p not in cited_personas]

    return {
        "valid": len(unreferenced) == 0,
        "unreferenced": unreferenced,
        "uncited": uncited,
    }


def generate_citation_appendix(
    persona_findings: list[dict], sources: list[dict] | None = None
) -> str:
    lines = ["## Bronvermelding / Sources", ""]

    if persona_findings:
        lines += [
            "### Persoonlijke bronnen / Persona sources",
            "",
            "| Persona | Rol | Autoriteit |",
            "|---------|-----|------------|",
        ]
        for pf in persona_findings:
            authority = AUTHORITY_NL.get(pf.get("authority", ""), "-")
            lines.append(f"| {pf['name']} | {pf['role']} | {authority} |")
        lines.append("")

    if sources:
        lines += [
            "### Kennisbronnen / Knowledge sources",
            "",
            "| ID | Bron / Source | Type | Relevantie / Relevance |",
            "|----|---------------|------|------------------------|",
        ]
        for s in sources:
            lines.append(
                f"| {s['id']} | {s.get('title', s['id'])} | {s.get('type', '-')} | {s.get('relevance', '-')} |"
            )
        lines.append("")

    return "\n".join(lines)


def citation_constraint_prompt(
    persona_findings: list[dict] | None = None, sources: list[dict] | None = None
) -> str:
    names = ", ".join(pf["name"] for pf in (persona_findings or [])) or "(none)"
    ids = ", ".join(s["id"] for s in (sources or [])) or "(none)"

    return (
        f"CITATION RULES — YOU MUST FOLLOW THESE:\n\n"
        f"1. Every factual claim MUST cite its source.\n"
        f"2. Use [§P:PersonaName] for claims from persona assessments. Available: {names}.\n"
        f"3. Use [§K:source-id] for claims from knowledge retrieval. Available: {ids}.\n"
        f'4. If you cannot verify a claim from available sources, write "[VERIFY]" after it.\n'
        f"5. Do NOT fabricate source IDs. Only use sources listed above.\n"
        f"6. Do NOT make claims without citations. Uncited claims will be flagged for human review."
    )


