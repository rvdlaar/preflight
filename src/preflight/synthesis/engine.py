def build_context(
    proposal_name: str = "[voorstel naam]",
    request_type: str = "new-application",
    ratings: dict[str, str] | None = None,
    persona_findings: list[dict] | None = None,
    triage: dict | None = None,
    zira: dict | None = None,
    biv: dict | None = None,
    assessment_mode: str = "fast",
    landscape: dict | None = None,
) -> dict[str, Any]:
    ratings = ratings or {}
    persona_findings = persona_findings or []
    triage = triage or {}
    zira = zira or {}
    biv = biv or {}

    today = date.today().isoformat()
    blocks = [p for p in persona_findings if p.get("rating") == "block"]
    conditions = [p for p in persona_findings if p.get("rating") == "conditional"]
    veto_used = next((p for p in blocks if p.get("authority") == "VETO"), None)
    escalation_used = next(
        (p for p in blocks if p.get("authority") == "ESCALATION"), None
    )
    fg_block = next((p for p in blocks if p.get("authority") == "INDEPENDENT"), None)

    return {
        "proposal_name": proposal_name,
        "date": today,
        "version": "0.1",
        "status": "DRAFT",
        "assessment_mode": "Deep (panel)"
        if assessment_mode == "deep"
        else "Fast (batched)",
        "psa_id": f"PSA-{today.replace('-', '')}",
        "biv_b": biv.get("B", "_"),
        "biv_i": biv.get("I", "_"),
        "biv_v": biv.get("V", "_"),
        "biv_rpo": biv.get("rpo", "[RPO]"),
        "biv_rto": biv.get("rto", "[RTO]"),
        "treatment": triage.get("treatment", "[triage]"),
        "triage_reason": triage.get("reason", ""),
        "recommendation": _derive_recommendation(ratings, blocks),
        "board_time": _derive_board_time(ratings, triage),
        "zira_domain": zira.get(
            "domain", "[Zorg | Onderzoek | Onderwijs | Sturing | Bedrijfsondersteuning]"
        ),
        "zira_domain_secondary": zira.get("secondaryDomain", ""),
        "veto_exercised": f"Ja — door {veto_used['name']}: {(veto_used.get('findings') or [''])[0]}"
        if veto_used
        else "Nee",
        "escalation": f"Ja — door {escalation_used['name']}: {(escalation_used.get('findings') or [''])[0]}"
        if escalation_used
        else "Nee",
        "fg_determination": "Concept — bevestiging door FG vereist"
        if fg_block
        else "N.v.t.",
        "block_count": len(blocks),
        "condition_count": len(conditions),
    }


def _derive_recommendation(ratings: dict, blocks: list[dict]) -> str:
    if any(b.get("authority") in ("VETO", "INDEPENDENT") for b in blocks):
        return "Afwijzen"
    if blocks:
        return "Uitstellen"
    if sum(1 for r in ratings.values() if r == "concern") >= 2:
        return "Goedkeuren met voorwaarden"
    if any(r == "conditional" for r in ratings.values()):
        return "Goedkeuren met voorwaarden"
    return "Goedkeuren"


def _derive_board_time(ratings: dict, triage: dict) -> str:
    if triage.get("treatment") == "deep-review":
        return "volledige sessie"
    if triage.get("treatment") == "standard-review":
        return "30 min"
    return "15 min"


