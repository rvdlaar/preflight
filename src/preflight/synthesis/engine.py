PRODUCT_TRIGGERS: dict[str, set[str]] = {
    "psa": {"always"},
    "adr": {"always"},
    "security-assessment": {"security-concern", "security-block"},
    "bia-biv": {"biv-high"},
    "dpia": {"patient-data", "clinical-system", "fg-dpo-concern", "fg-dpo-block"},
    "clinical-impact": {"clinical-system"},
    "integration-design": {"integration", "integration-concern", "integration-block"},
    "network-impact": {"network-concern", "network-block"},
    "process-impact": {"process-concern", "process-block"},
    "eu-ai-act": {"ai-ml"},
    "vendor-assessment": {"vendor-selection"},
    "nfr-specification": {"solution-concern", "solution-block"},
    "operational-readiness": {"always"},
    "tech-radar-update": {"new-application", "infrastructure-change"},
    "roadmap-impact": {"portfolio-concern", "portfolio-block", "architecture-roadmap"},
    "decommission-checklist": {"decommission"},
}


def determine_required_documents(
    request_type: str, ratings: dict[str, str] | None = None, biv: dict | None = None
) -> list[str]:
    ratings = ratings or {}
    biv = biv or {}

    active_triggers: set[str] = {"always", request_type}

    for persona_id, rating in ratings.items():
        if rating in ("block", "concern"):
            active_triggers.add(f"{persona_id}-block")
            active_triggers.add(f"{persona_id}-concern")

    if any(biv.get(k, 0) >= 3 for k in ("B", "I", "V")):
        active_triggers.add("biv-high")

    docs: set[str] = set()
    for doc_name, triggers in PRODUCT_TRIGGERS.items():
        if active_triggers & triggers:
            docs.add(doc_name)

    return sorted(docs)


def generate_document(template_name: str, context: dict) -> str:
    template = load_template(template_name)
    return resolve_placeholders(template, context)


def generate_all_documents(input: dict) -> dict[str, str]:
    context = build_context(
        **{
            k: v
            for k, v in input.items()
            if k
            in (
                "proposal_name",
                "request_type",
                "ratings",
                "persona_findings",
                "triage",
                "zira",
                "biv",
                "assessment_mode",
                "landscape",
            )
        }
    )
    required = determine_required_documents(
        input.get("request_type", "new-application"),
        input.get("ratings"),
        input.get("biv"),
    )

    documents = {}
    for name in required:
        try:
            documents[name] = generate_document(name, context)
        except FileNotFoundError:
            pass

    return documents


